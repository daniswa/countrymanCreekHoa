                            <h3 class='bodyTitle'>Chairman's Report</h3>
                            <br>
                            <p class='bodyText'>Brush Pile was burnt, thanks were extended to Courtney and Bob for taking care of it.</p>
                            <p class='bodyText'>Clark hill had drainage and culvert improvements performed.</p>
                            <p class='bodyText'>A new snow plow, motor, and hydraulics were installed onto the Fire truck vehicle. A sand
                            spreader was also purchased and installed on the back of the truck.</p>
                            <p class='bodyText'>Request was made for assistance running the snow plow in the winter.</p>
                            <p class='bodyText'>Perry H. questioned if multiple bids were requested for the work performed on Clark.
                            Attempts have been made in the past to obtain other bids, it is difficult to find contractors
                            willing to work with the HOA, our availability of funds seems to be an issue.</p>
                            <p class='bodyText'>Annual Meeting Minutes Approval: Bob P. motion to accept minutes, Mike K. seconded
                            the motion. No Nays. Weed spraying was not done; a new contractor still needs to be selected.</p>
                            <p class='bodyText'>Financial Report/Budget Approval: Don motioned to accept financial report, Courtney
                            seconded. Courtney Motioned to approve budget, Bob seconded motion. All vote to
                            approve- no nays. bank accounts are still in the process of being changed over, assets are
                            to be re-evaluated this year- finalized budget and asset sheet can be emailed/mailed upon
                            request. Electrical was questioned- The CCR HOA pays electric on 3 meter heads total, 2
                            wells, and the vehicle shed. Delinquent accounts were reduced from 17 in 2015 to 9 this
                            year. 1 account may be brought current in the next few months, and 2 accounts are making
                            payments. Member requested the total amount received from accounts brought current in
                            2015, information was not readily available.</p>
                            <p class='bodyText'>One Board Position open: Perry Helt nominated Tom Kessler, Bob Purvayn seconded.
                            Member body approved, no nays. Cindy Epperson nominated Don Holzheimer who
                            declined nomination.</p>
                            <h3 class='bodyTitle'>New Business</h3>
                            <p class='bodyText'>Perry H. brought up adding backfill to the plug area where the logjam was removed, his
                            concern being that a wetland was being created and if another logjam were to form it would
                            not be possible to get a permit to remove it if a wetland was established.</p>
                            <p class='bodyText'>Roads: signs are needed to ensure outside motorists and contractors/semi-trucks can see
                            that there is no outlet in Countryman Creek, new road signs will be put up in the next few
                            months. Bids were received from 406 excavating to perform road reconstruction on Clark
                            Dr. from the mailboxes to the bridge for 27,000 and to reconstruct/repair 7 miles of roads for
                            15,000. Don motioned to perform a test patch from the bottom of Clark Dr. to the bridge.
                            Perry seconded motion. No official vote was taken, but no dissent was heard.</p>
                            <p class='bodyText'>Insurance- new company (PayneWest) taking over insurance coverage, update liability
                            insurance.</p>
                            <p class='bodyText'>Discussion regarding trash and partying on the state ground by the river was brought up.
                            Gates can not be installed on state ground by river. Place a sign for property owners
                            only/no trespassing? Be more diligent? Watch for untoward activities occurring and
                            unknown vehicles.</p>
                            <p class='bodyText'>Board requested a 3% increase in dues, member body approved, no dissent.</p>
                            <p class='bodyText'>Don Holzheimer brought discussion on cows- NRCS recommendation was no grazing on
                            burn area for 2-3 years. It is too late to re-negotiate lease this year, cattle will be release the
                            week of May 15. - fencing issue was brought up - Cattle owner is responsible for
                            maintaining external boundary fence, Property owners are responsible for fencing cattle out.
                            Member requested permission to contact D. Saunders regarding the fence boundary close
                            to the owner's home. Permission was granted, agreed to also provide copy of cattle lease.</p>
                            <p class='bodyText'>Bob P. brought up a warning story about relaxing covenants, and suggested that the
                            property lenders could take legal action against the HOA if they felt property values were
                            being placed at risk.</p>
                            <p class='bodyText'>Cindy E. requested permission to contact Beartooth Electric to remove an old, defunct
                            power line. Permission was granted.</p>
                            <p class='bodyText'>Request for financial documentation was made by Perry Helt.</p>
                            <p class='bodyText'>Cindy E. motioned to adjourn meeting at 11:11 am, Therese F. seconded.</p>
